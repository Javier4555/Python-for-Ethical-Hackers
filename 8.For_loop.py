# Hello everyone! , we are learning how to implement a 'For loop' in python.

# A "for loop" is like a repetitive task manager in programming. It helps you do the same thing multiple times, like counting or performing an action on a bunch of items.

# we take a simple example to understand :-
# find a number which is divisible by 2 between 0 and 20 ?

for num in range(1,20,):  
   if num % 2 == 0:
     print(num)


