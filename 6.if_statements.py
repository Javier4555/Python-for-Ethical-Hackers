# Hello everyone! , we are learning how to write a 'Conditional statements' in python

# Conditional statements allow a program to make decisions based on whether a certain condition is true or false.

first_number = input("The value of first_number is: ")
second_number = input("The value of second_number is: ")

if first_number > second_number:
    print("The value of first_number is larger")

if first_number < second_number:
    print("The value of second_number are larger")

else:
    print("Both value's are same")

